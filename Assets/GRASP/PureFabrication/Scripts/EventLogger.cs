using UnityEngine;

/*
 * Паттерн: Искусственный (вымышленный) объект
 * Применение: класс, созданный для улучшения проектных характеристик (связность, повторное использование и т.д.).
 * Вынесен за пределы доменной модели и занимается логированием событий. Это снижает нагрузку на основные сущности.
 * Плюсы: изоляция технических деталей, простота замены механизма логирования.
 */

namespace Grasp.PureFabricationPattern
{
    // Класс не относится к домену, но выделяет ответственность по логированию
    public static class EventLogger
    {
        public static void Log(string message)
        {
            Debug.Log($"[Spaceport] {message}");
        }
    }
}
